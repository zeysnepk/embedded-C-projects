

int nums_add(int num1, int num2){
	return num1 + num2;
}

int nums_sub(int num1, int num2){
	return num1 - num2;
}

long long int nums_mul(int num1, int num2){
	return (long long int)num1 * num2; //typecasting: int to long long int
}

float nums_div(int num1, int num2){
	return (float)num1 / num2; //typecasting: int to float
}
